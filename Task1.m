# Определение целевой функции
function y = f(x)
  y = x(1) + 2*x(2) - x(3) - x(4);
endfunction

# Определение функции ограничений (равенства)
function r = g(x)
  r = [2*x(1) - x(2) + x(3) - 3*x(4) - 8;
       x(1) + x(2) - 2*x(3) + x(4) - 12];
endfunction

# Начальное приближение
x0 = [1; 1; 1; 1];  # Любое начальное значение

# Определение функции Task1, которая вызывает sqp
function Task1(x)
  [xopt, obj, info, iter] = sqp(x, @f, @g, [], [], [], 100, 1e-6);
  disp("Оптимальные значения:");
  disp(xopt);
  disp("Значение целевой функции:");
  disp(obj);
endfunction

# Вызов функции Task1 с начальным приближением
Task1(x0);

